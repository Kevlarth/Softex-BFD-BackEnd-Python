# Peça dois valores ao usuário: Largura e Altura.
# Calcule e mostre a área do retângulo.

print("===== Descobrir Área do Retângulo ======")
largura = int(input("Digite a largura do retângulo: "))
altura = int(input("Digite a altura do retângulo: "))

print(f"===== Resultado =====\nA Área do retângulo de altura {altura} e largura {largura} é: {altura*largura}")