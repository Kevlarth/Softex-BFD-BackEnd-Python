# Crie um script que mostre o resultado das operações abaixo:
# Use as variáveis a = 10 e b = 3
# soma, subtração, multiplicação, divisão, divisão inteira, resto da divisão e potência.

a = 10
b = 3

print(f"Soma: {a+b}")
print(f"Subtração: {a-b}")
print(f"Multiplicação: {a*b}")
print(f"Divisão: {a/b}")
print(f"Divisão Inteira: {a//b}")
print(f"Resto da divisão: {a%b}")
print(f"Potência: {a**b}")